package basic;

public class Basic01 {
//		public : 클래스에 접근권한중 하나이며 Basic01 클래스는 누구나 접근이 가능하다
//		class : 객체를 생성하기 위한 설계도
	public static void main(String[] args) {
//		static : 객체생성없이 main()을 사용할 수 있다
//		void : 리턴해줄 값이 없을 때 사용
//		main() : 메서드라고 부른다, ..을 실행하는 기능이다, 모든 메서드는 ()이
//		String[] args : 매개변수라고 부른다
		
//		문자, 숫자, 계산된 값을 출력하고 싶어요
		System.out.println("i love you");
		// 문자열 : 쌍따옴표를 사용한다.
		System.out.println("1 + 1");
		
		System.out.println(1 + 1);
		
		System.out.println('A');
		
		System.out.println();
	}

}