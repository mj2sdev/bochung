package basic;

public class Basic12 {
	public static void main(String[] args) {
		
		int a = 50, b = 30;
		// 큰가?
		
		System.out.println("a > b : " + (a > b));
		// 작은가?
		System.out.println("a < b : " + (a < b));
		// 같은가?
		System.out.println("a == b : " + (a == b));
		// 같지 않은가?
		System.out.println("a == b : " + (a != b));
		// 크거나 같은가?
		System.out.println("a == b : " + (a >= b));
		// 작거나 같은가?
		System.out.println("a == b : " + (a <= b));
	
	}
}
